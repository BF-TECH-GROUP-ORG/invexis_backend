// Auto-generate scanning & identifier fields (async to ensure uniqueness)
productSchema.pre("save", async function (next) {
  try {
    const Product = this.constructor;

    // Helper to check uniqueness with limited attempts
    const ensureUnique = async (field, genFn, maxAttempts = 5) => {
      let attempts = 0;
      let candidate;
      while (attempts < maxAttempts) {
        candidate = genFn();
        // eslint-disable-next-line no-await-in-loop
        const exists = await Product.findOne({ [field]: candidate }).lean();
        if (!exists) return candidate;
        attempts += 1;
      }
      // If we couldn't find a unique candidate after attempts, return last candidate
      return candidate;
    };

    // SKU - try to create a readable SKU using companyId/slug + random suffix
    if (!this.sku) {
      this.sku = await ensureUnique('sku', () => {
        const prefix = (this.companyId || 'CMP').toString().replace(/[^A-Za-z0-9]/g, '').toUpperCase().slice(0, 4) || 'CMP';
        const base = (this.slug || this.name || '').toString().toUpperCase().replace(/[^A-Z0-9]/g, '').slice(0, 8) || Date.now().toString().slice(-6);
        const suffix = Math.random().toString(36).substring(2, 6).toUpperCase();
        return `${prefix}-${base}-${suffix}`.slice(0, 64);
      });
    }

    // barcode: display value should be SKU. Use SKU when available, otherwise generate one.
    if (!this.barcode) {
      // prefer SKU (which is unique) for human-readable barcode display
      this.barcode = this.sku || await ensureUnique('barcode', () => `BC${Date.now()}${Math.floor(Math.random() * 9999)}`);
    }

    // Create full product payload and encode as base64 for embedding into QR / barcode payloads.
    try {
      // toObject gives a plain object representation; exclude mongoose internals
      const obj = this.toObject ? this.toObject({ depopulate: true, virtuals: false }) : Object.assign({}, this);
      // It's safe to remove mongoose __v if present
      if (obj.__v !== undefined) delete obj.__v;
      const encoded = Buffer.from(JSON.stringify(obj)).toString("base64");

      // QR code should contain the full product data encoded
      this.qrCode = encoded;
      this.qrPayload = encoded;

      // Barcode should display SKU but carry the full encoded payload in barcodePayload
      this.barcodePayload = encoded;
    } catch (e) {
      // If encoding fails for any reason, fallback to lightweight codes
      if (!this.qrCode) this.qrCode = `QR${Date.now()}${Math.floor(Math.random() * 9999)}`;
      if (!this.qrPayload) this.qrPayload = this.qrCode;
    }

    // scanId
    if (!this.scanId) {
      this.scanId = await ensureUnique('scanId', () => `SCAN-${Math.random().toString(36).substring(2, 10).toUpperCase()}`);
    }

    // ASIN - auto-generate if missing. Use a compact alphanumeric pattern.
    if (!this.asin) {
      this.asin = await ensureUnique('asin', () => {
        const hash = Math.random().toString(36).substring(2, 8).toUpperCase();
        return `ASIN${Date.now().toString().slice(-6)}${hash}`;
      });
    }

    // UPC - 12 digit numeric code (generate random; uniqueness checked)
    if (!this.upc) {
      this.upc = await ensureUnique('upc', () => {
        const num = Math.floor(Math.random() * 1e12).toString().padStart(12, '0');
        return num;
      });
    }

    // browseNodeId - low-collision identifier used for browsing trees
    if (!this.browseNodeId) {
      this.browseNodeId = `BN${Date.now().toString().slice(-8)}${Math.random().toString(36).substring(2, 6).toUpperCase()}`;
    }

    return next();
  } catch (err) {
    return next(err);
  }
});

// Update availability if scheduled
productSchema.pre("save", function (next) {
  this.updatedAt = new Date();

  if (
    this.scheduledAvailabilityDate &&
    new Date() < this.scheduledAvailabilityDate
  ) {
    this.availability = "scheduled";
  } else if (
    this.scheduledAvailabilityDate &&
    new Date() >= this.scheduledAvailabilityDate
  ) {
    this.availability = "in_stock";
  }

  next();
});

// Validate category hierarchy
productSchema.pre("save", async function (next) {
  // Ensure assigned category exists and is a level 3 category.
  if (this.isModified("category")) {
    const Category = mongoose.model("Category");
    const category = await Category.findById(this.category);
    if (!category || category.level !== 3) {
      return next(new Error("Invalid category selection: category must be level 3"));
    }

    // If the category is scoped to a company, ensure product.companyId matches
    if (category.companyId && this.companyId && category.companyId !== this.companyId) {
      return next(new Error("Category companyId does not match product companyId"));
    }
  }
  next();
});

