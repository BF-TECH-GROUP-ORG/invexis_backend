/**
 * Return merged attributes for a variation sku.
 * Product attributes provide defaults/templates; variation attributes override them.
 * @param {String} sku
 * @returns {Object} flat map of attribute name -> value
 */
productSchema.methods.getEffectiveAttributesForSku = function (sku) {
  const productAttrs = (this.attributes || []).reduce((m, a) => {
    if (a && a.name) m[a.name] = a.value;
    return m;
  }, {});

  if (!sku) return productAttrs;

  const variation = (this.variations || []).find(v => v.sku === sku);
  if (!variation) return productAttrs;

  const variationAttrs = (variation.attributes || []).reduce((m, a) => {
    if (a && a.name) m[a.name] = a.value;
    return m;
  }, {});

  return Object.assign({}, productAttrs, variationAttrs);
};


